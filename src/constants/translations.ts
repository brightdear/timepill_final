export const translations: Record<Lang, typeof ko> = { ko, en, ja }

export type TranslationKey = keyof typeof ko
export type TranslationKeys = TranslationKey
export type TranslationCopy = typeof ko

function interpolate(template: string, params?: Record<string, string | number | null | undefined>) {
  if (!params) return template

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key]
    return value === undefined || value === null ? '' : String(value)
  })
}

export function translate(
  lang: Lang,
  key: TranslationKey,
  params?: Record<string, string | number | null | undefined>,
) {
  return interpolate(translations[lang][key], params)
}
